const fs = require('fs');
const path = require('path');

const projectRoot = path.resolve(__dirname, '..');
const studentsDir = path.join(projectRoot, 'app', 'students');
const generatedDir = path.join(projectRoot, 'generated');
const outFile = path.join(generatedDir, 'students-registry.ts');

function toId(basename) {
  return basename
    .replace(/\.(tsx|ts|jsx|js)$/i, '')
    .replace(/[^a-zA-Z0-9]+/g, '-')
    .replace(/(^-+|-+$)/g, '')
    .toLowerCase();
}

function toTitle(id) {
  return id
    .split('-')
    .filter(Boolean)
    .map((p) => p.charAt(0).toUpperCase() + p.slice(1))
    .join(' ');
}

function main() {
  if (!fs.existsSync(studentsDir)) {
    console.error(`Students directory not found: ${studentsDir}`);
    process.exit(1);
  }

  if (!fs.existsSync(generatedDir)) {
    fs.mkdirSync(generatedDir, { recursive: true });
  }

  const entries = fs
    .readdirSync(studentsDir, { withFileTypes: true })
    .filter((d) => d.isFile())
    .map((d) => d.name)
    .filter((name) => /\.(tsx|ts|jsx|js)$/i.test(name))
    .filter((name) => !/\.d\.ts$/i.test(name))
    .filter((name) => !/^registry\.(tsx|ts|jsx|js)$/i.test(name))
    .sort((a, b) => a.localeCompare(b));

  const imports = [];
  const items = [];

  for (const filename of entries) {
    const base = filename.replace(/\.(tsx|ts|jsx|js)$/i, '');
    const id = toId(base);
    const name = toTitle(id);
    const importName = `${base.replace(/[^a-zA-Z0-9_$]/g, '_')}Screen`;
    imports.push(`import ${importName} from '../app/students/${base}';`);
    items.push(`  { id: '${id}', name: '${name}', Component: ${importName} },`);
  }

  const content = `/* eslint-disable */
// This file is auto-generated by scripts/generate-students-registry.js
// Do not edit manually.

import type React from 'react';

${imports.join('\n')}

export type StudentConfig = {
  id: string;
  name: string;
  Component: React.ComponentType;
};

export const students: StudentConfig[] = [
${items.join('\n')}
];
`;

  fs.writeFileSync(outFile, content, 'utf8');
  console.log(`Generated ${path.relative(projectRoot, outFile)} with ${entries.length} student(s).`);
}

main();

